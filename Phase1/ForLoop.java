class ForLoop{
    public static void main(String[] args) {
        //3 ส่วน 
        // ส่วนตัวแปรเริ่มต้น => ตัวนับรอบ
        // เงื่อนไข 
        // การเพิ่มค่า / ลดค่า
        for(int count=10;count>0;count-=2){
            // คำสั่งที่จะทำซ้ำ
            System.out.println(count);
            // if(count>0)break;
            // 10 , 9
        }
    }
}
